# === Windows: Copy brand-specific app icon ===
BRAND_WIN_ICON="$ROOT_DIR/assets/brands/$BRAND/app_icon.ico"
WIN_ICON="$ROOT_DIR/windows/runner/resources/app_icon.ico"
if [[ ! -f "$BRAND_WIN_ICON" ]]; then
  echo "  ERROR: missing Windows app icon: $BRAND_WIN_ICON" >&2
  exit 1
fi
cp "$BRAND_WIN_ICON" "$WIN_ICON"
if ! cmp -s "$BRAND_WIN_ICON" "$WIN_ICON"; then
  echo "  ERROR: Windows runner icon does not match brands/$BRAND/app_icon.ico after copy" >&2
  exit 1
fi
echo "  Windows: app_icon.ico copied from brands/$BRAND/ and verified"

# === Linux: Copy brand-specific app icon ===
BRAND_LINUX_ICON="$ROOT_DIR/assets/brands/$BRAND/app_icon.png"
LINUX_ICON="$ROOT_DIR/linux/icons/app_icon.png"
if [[ -f "$BRAND_LINUX_ICON" ]]; then
  mkdir -p "$(dirname "$LINUX_ICON")"
  cp "$BRAND_LINUX_ICON" "$LINUX_ICON"
  echo "  Linux: app_icon.png copied from brands/$BRAND/"
fi

# === Linux: Update CMakeLists.txt BINARY_NAME + PRODUCT_DISPLAY_NAME + APPLICATION_ID + BRAND_NAME_LOWER ===
LINUX_CMAKE="$ROOT_DIR/linux/CMakeLists.txt"
if [[ -f "$LINUX_CMAKE" ]]; then
  if [[ "$BRAND" == "vidcombo" ]]; then
    sedi 's/set(BINARY_NAME "svid")/set(BINARY_NAME "vidcombo")/' "$LINUX_CMAKE" 2>/dev/null || true
    sedi 's/set(PRODUCT_DISPLAY_NAME "Svid")/set(PRODUCT_DISPLAY_NAME "VidCombo")/' "$LINUX_CMAKE" 2>/dev/null || true
    sedi 's/set(APPLICATION_ID "com.svid.app")/set(APPLICATION_ID "com.tinasoft.vidcombo")/' "$LINUX_CMAKE" 2>/dev/null || true
    sedi 's/set(BRAND_NAME_LOWER "svid")/set(BRAND_NAME_LOWER "vidcombo")/' "$LINUX_CMAKE" 2>/dev/null || true
  else
    sedi 's/set(BINARY_NAME "vidcombo")/set(BINARY_NAME "svid")/' "$LINUX_CMAKE" 2>/dev/null || true
    sedi 's/set(PRODUCT_DISPLAY_NAME "VidCombo")/set(PRODUCT_DISPLAY_NAME "Svid")/' "$LINUX_CMAKE" 2>/dev/null || true
    sedi 's/set(APPLICATION_ID "com.tinasoft.vidcombo")/set(APPLICATION_ID "com.svid.app")/' "$LINUX_CMAKE" 2>/dev/null || true
    sedi 's/set(BRAND_NAME_LOWER "vidcombo")/set(BRAND_NAME_LOWER "svid")/' "$LINUX_CMAKE" 2>/dev/null || true
  fi
  echo "  Linux: CMakeLists.txt BINARY_NAME + PRODUCT_DISPLAY_NAME + APPLICATION_ID updated"
fi

echo ""
echo "Brand set to: $BRAND"
echo "Build commands:"
echo "  macOS:   fvm flutter build macos --dart-define=BRAND=$BRAND"
echo "  Windows: flutter build windows --dart-define=BRAND=$BRAND"
echo "  Linux:   flutter build linux --dart-define=BRAND=$BRAND"
echo "  Package: bash scripts/package_linux.sh $BRAND"
